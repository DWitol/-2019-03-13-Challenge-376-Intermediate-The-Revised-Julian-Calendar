def leaps(x,y):
    x -=1
    y -=1
    #leap years up to x
    x = x//4 - x//100 +max(0,(x-600))//900 + max(0,(x-200)//900)
    y = y//4 - y//100 +max(0,(y-600))//900 + max(0,(y-200)//900)
    return y-x

inputs = [[2016, 2017], [2019, 2020], [ 1900, 1901],
          [2000, 2001], [2800, 2801], [123456, 123456],
          [1234, 5678], [123456, 7891011],[123456789101112, 1314151617181920]]

for n in inputs:print(leaps(n[0],n[1]))

